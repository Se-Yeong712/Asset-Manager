package aca.mirim.domain;

public class Impulse {
	
	private int one;
	private int two;
	private int three;
	private int four;
	public int getOne() {
		return one;
	}
	public void setOne(int one) {
		this.one = one;
	}
	public int getTwo() {
		return two;
	}
	public void setTwo(int two) {
		this.two = two;
	}
	public int getThree() {
		return three;
	}
	public void setThree(int three) {
		this.three = three;
	}
	public int getFour() {
		return four;
	}
	public void setFour(int four) {
		this.four = four;
	}
	@Override
	public String toString() {
		return "Impulse [one=" + one + ", two=" + two + ", three=" + three + ", four=" + four + "]";
	}
	
	

}
